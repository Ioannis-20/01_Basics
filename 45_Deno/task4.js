let age = Number(prompt("Write down your age"));

if (age < 18 ){
    console.log("You are young");
}else{
    console.log("you are not young");
}

if (age >= 18 && age < 50){
    console.log("you are middle aged");
}else{
    console.log("you are not middle aged");
}

if (age > 50){
    console.log("you are old")
}else{
    console.log("you are not old")
}